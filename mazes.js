export function generateSeenArray(width, height) {
    let result = [];
    for (let y = 0; y < height; y++) {
        result[y] = [];
        for (let x = 0; x < width; x++) {
            if (x === 0 || y === 0 || x === width - 1 || y === height - 1 || (x < 3 && y < 3)) {
                result[y][x] = 1;
            } else {
                result[y][x] = 0;
            }
        }
    }
    return result;
}